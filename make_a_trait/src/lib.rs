pub trait Summarizable {
    fn summary(&self) -> String;
    // each type that implements this trait must provide
    // its own custom behavior for the body of the method
    // BUT the compiler will enforce that any type with
    // the `Summarizable` trait will have the method
    // `summary` defined for it with this signature exactly
}

pub struct NewsArticle {
    pub headline: String,
    pub location: String,
    pub author: String,
    pub content: String,
}

impl Summarizable for NewsArticle {
    fn summary(&self) -> String {
        format!("{}, by {} ({})", self.headline, self.author, self.location)
    }
}

pub struct Tweet {
    pub username: String,
    pub content: String,
    pub reply: bool,
    pub retweet: bool,
}

impl Summarizable for Tweet {
    fn summary(&self) -> String {
        format!("{}: {}", self.username, self.content)
    }
}

